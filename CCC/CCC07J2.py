a = ''
while True:
    a = input()
    if a == 'CU':
        print('see you')
    elif a == ':-)':
        print("I'm happy")
    elif a == ':-(':
        print("I'm unhappy")
    elif a == ';-)':
        print('wink')
    elif a == ':-P':
        print('stick out my tongue')
    elif a == '(~.~)':
        print('sleepy')
    elif a == 'TA':
        print('totally awesome')
    elif a == 'CCC':
        print('Canadian Computing Competition')
    elif a == 'CUZ':
        print('because')
    elif a == 'TY':
        print('thank-you')
    elif a == 'YW':
        print("you're welcome")
    elif a == 'TTYL':
        print('talk to you later')
        break
    else:
      print(a)